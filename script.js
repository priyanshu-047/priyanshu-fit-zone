// RENDER WORKOUTS
// =========================

// =========================
// CURRENT DAY INDEX
// =========================

const weekDays = [

  "Sunday",
  "Monday",
  "Tuesday",
  "Wednesday",
  "Thursday",
  "Friday",
  "Saturday"

];

const todayName =
weekDays[new Date().getDay()];

// Find today's workout index
let currentDayIndex =
workouts.findIndex(
  day => day.day === todayName
);

// Fallback
if(currentDayIndex === -1){

  currentDayIndex = 0;

}

// =========================
// ELEMENTS
// =========================

const currentDayTitle =
document.getElementById("currentDayTitle");

const prevDay =
document.getElementById("prevDay");

const nextDay =
document.getElementById("nextDay");

// =========================
// DAY NAVIGATION
// =========================

if(prevDay){

  prevDay.onclick = ()=>{

    currentDayIndex--;

    if(currentDayIndex < 0){

      currentDayIndex =
      workouts.length - 1;

    }

    renderCurrentDay();

  };

}

if(nextDay){

  nextDay.onclick = ()=>{

    currentDayIndex++;

    if(currentDayIndex >= workouts.length){

      currentDayIndex = 0;

    }

    renderCurrentDay();

  };

}

// =========================
// RENDER CURRENT DAY
// =========================

function renderCurrentDay(){

  if(!workoutList) return;

  workoutList.innerHTML = "";

  const dayData =
  workouts[currentDayIndex];

  currentDayTitle.innerText =
  dayData.day;

  dayData.workouts.forEach((workout,index)=>{

    const uniqueId =
    `${currentDayIndex}-${index}`;

    const checked =
    completed.includes(uniqueId);

    const card =
    document.createElement("div");

    card.className =
    checked
    ? "card completed"
    : "card";

    card.innerHTML = `

      <div class="video-container">

        <img
          src="https://img.youtube.com/vi/${getYoutubeId(workout.video)}/hqdefault.jpg"
          class="video-thumb"
        >

        <button
          class="play-btn"
          onclick="loadVideo(this,'${workout.video}')"
        >
          ▶
        </button>

      </div>

      <h2>
        ${workout.name}
      </h2>

      <p>
        ${workout.sets} Sets • ${workout.reps}
      </p>

      <input
        type="checkbox"
        class="check"
        ${checked ? "checked" : ""}
        onchange="toggleWorkout('${uniqueId}')"
      >

    `;

    workoutList.appendChild(card);

  });

  updateProgress();

}

// =========================
// TOTAL WORKOUT COUNT
// =========================

function getTotalWorkoutCount(){

  let total = 0;

  workouts.forEach(day=>{

    total += day.workouts.length;

  });

  return total;

}

// =========================
// UPDATE PROGRESS
// =========================

function updateProgress(){

  const total =
  getTotalWorkoutCount();

  const percent =
  (completed.length / total) * 100;

  progress.style.width =
  `${percent}%`;

  progressText.innerText =
  `${Math.round(percent)}% Completed`;

}

// =========================
// TOGGLE WORKOUT
// =========================

function toggleWorkout(id){

  if(completed.includes(id)){

    completed =
    completed.filter(i=>i!==id);

  }else{

    completed.push(id);

  }

  localStorage.setItem(
    "completed",
    JSON.stringify(completed)
  );

  renderCurrentDay();

}

